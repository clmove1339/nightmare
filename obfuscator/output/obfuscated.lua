            end;

            local in_move = bit.band(cmd.buttons, IN.BACK + IN.FORWARD + IN.MOVELEFT + IN.MOVERIGHT) ~= 0;
            if in_move then
                return;
            end;

            local flags = me:get_flags();
            if bit.hasnt(flags, FL.ONGROUND) or bit.has(cmd.buttons, IN.JUMP) then
                return;
            end;

